public class Main_584 {
    public static void main(String[] args) {

        int[] arr = {1, 2, 3};

        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {

                System.out.println(arr[i] + " + " + arr[j] + " = " + (arr[i] + arr[j]));

            }
        }

    }


}
